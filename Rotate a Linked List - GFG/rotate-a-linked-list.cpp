class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
        Node*p=head;
        while(p!=NULL){
            if(p->next==NULL) {
                p->next=head;
                break;
            }
            p=p->next;
        }
        int c=1;
        p=head;
        while(c!=k){
            p=p->next;
            c++;
        }
        Node*ans=p->next;
        p->next=NULL;
        return ans;
    }
};
    


